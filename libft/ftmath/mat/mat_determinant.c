#include "mat.h"

inline float	mat3f_determinant(const float m[3][3])
{
	return (m[0][0] * m[1][1] * m[2][2]
	- m[0][0] * m[1][2] * m[2][1]
	- m[0][1] * m[1][0] * m[2][2]
	+ m[0][1] * m[1][2] * m[2][0]
	+ m[0][2] * m[1][0] * m[2][1]
	- m[0][2] * m[1][1] * m[2][0]);
}

inline double	mat3d_determinant(const double m[3][3])
{
	return (m[0][0] * m[1][1] * m[2][2]
	- m[0][0] * m[1][2] * m[2][1]
	- m[0][1] * m[1][0] * m[2][2]
	+ m[0][1] * m[1][2] * m[2][0]
	+ m[0][2] * m[1][0] * m[2][1]
	- m[0][2] * m[1][1] * m[2][0]);
}

inline float	mat4f_determinant(const float m[4][4])
{
	float	a00, a01, a02, a03;

	a00 = m[0][0] * (m[1][1] * m[2][2] * m[3][3]
	- m[1][1] * m[2][3] * m[3][2]
	- m[1][2] * m[2][1] * m[3][3]
	+ m[1][2] * m[2][3] * m[3][1]
	+ m[1][3] * m[2][1] * m[3][2]
	- m[1][3] * m[2][2] * m[3][1]);
	a01 = m[0][1] * (m[1][0] * m[2][2] * m[3][3]
	- m[1][0] * m[2][3] * m[3][2]
	- m[1][2] * m[2][0] * m[3][3]
	+ m[1][2] * m[2][3] * m[3][0]
	+ m[1][3] * m[2][0] * m[3][2]
	- m[1][3] * m[2][2] * m[3][0]);
	a02 = m[0][2] * (m[1][0] * m[2][1] * m[3][3]
	- m[1][0] * m[2][3] * m[3][1]
	- m[1][1] * m[2][0] * m[3][3]
	+ m[1][1] * m[2][3] * m[3][0]
	+ m[1][3] * m[2][0] * m[3][1]
	- m[1][3] * m[2][1] * m[3][0]);
	a03 = m[0][3] * (m[1][0] * m[2][1] * m[3][2]
	- m[1][0] * m[2][2] * m[3][1]
	- m[1][1] * m[2][0] * m[3][2]
	+ m[1][1] * m[2][2] * m[3][0]
	+ m[1][2] * m[2][0] * m[3][1]
	- m[1][2] * m[2][1] * m[3][0]);
	return (a00 - a01 + a02 - a03);
}

inline double	mat4d_determinant(const double m[4][4])
{
	double	a00, a01, a02, a03;

	a00 = m[0][0] * (m[1][1] * m[2][2] * m[3][3]
	- m[1][1] * m[2][3] * m[3][2]
	- m[1][2] * m[2][1] * m[3][3]
	+ m[1][2] * m[2][3] * m[3][1]
	+ m[1][3] * m[2][1] * m[3][2]
	- m[1][3] * m[2][2] * m[3][1]);
	a01 = m[0][1] * (m[1][0] * m[2][2] * m[3][3]
	- m[1][0] * m[2][3] * m[3][2]
	- m[1][2] * m[2][0] * m[3][3]
	+ m[1][2] * m[2][3] * m[3][0]
	+ m[1][3] * m[2][0] * m[3][2]
	- m[1][3] * m[2][2] * m[3][0]);
	a02 = m[0][2] * (m[1][0] * m[2][1] * m[3][3]
	- m[1][0] * m[2][3] * m[3][1]
	- m[1][1] * m[2][0] * m[3][3]
	+ m[1][1] * m[2][3] * m[3][0]
	+ m[1][3] * m[2][0] * m[3][1]
	- m[1][3] * m[2][1] * m[3][0]);
	a03 = m[0][3] * (m[1][0] * m[2][1] * m[3][2]
	- m[1][0] * m[2][2] * m[3][1]
	- m[1][1] * m[2][0] * m[3][2]
	+ m[1][1] * m[2][2] * m[3][0]
	+ m[1][2] * m[2][0] * m[3][1]
	- m[1][2] * m[2][1] * m[3][0]);
	return (a00 - a01 + a02 - a03);
}
